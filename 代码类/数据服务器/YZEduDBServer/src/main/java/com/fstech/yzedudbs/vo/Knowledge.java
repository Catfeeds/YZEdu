package com.fstech.yzedudbs.vo;

public class Knowledge {
	private Integer knowledge_id;
	private String knowledge_content;
	private Byte knowledge_rank;
	public Integer getKnowledge_id() {
		return knowledge_id;
	}
	public void setKnowledge_id(Integer knowledge_id) {
		this.knowledge_id = knowledge_id;
	}
	public String getKnowledge_content() {
		return knowledge_content;
	}
	public void setKnowledge_content(String knowledge_content) {
		this.knowledge_content = knowledge_content;
	}
	public Byte getKnowledge_rank() {
		return knowledge_rank;
	}
	public void setKnowledge_rank(Byte knowledge_rank) {
		this.knowledge_rank = knowledge_rank;
	}

	
}
